    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            String nombre = tbxnombre.getText().trim();
            float inicial = Float.parseFloat(snrinciial.getValue().toString());
            float mensualidad = Float.parseFloat(snrmensualidad.getValue().toString());
            String descripcion = txtdescripcion.getText().trim();
            boolean gua = ADNMenbresia.Guardar(new Menbresia(id, nombre, inicial, mensualidad,descripcion, true));
            
            if (gua) {
                if (FIListaMenbresia.modif) {
                    JOptionPane.showMessageDialog(rootPane, "Datos actualizados correctamente");
                    FIListaMenbresia.modif=false;
                }else
                {
                    JOptionPane.showMessageDialog(rootPane, "Datos guardados correctamente");
                }
                setVisible(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

