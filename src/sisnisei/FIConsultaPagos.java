    public void VerDatos() {
        try {
            int nf = jTable1.getRowCount();
            for (int i = 0; i < nf; i++) {
                dtm.removeRow(0);
            }
            String dni = tbxdni.getText();
            for (ListaPagos a : ADNPago.ListaPagos(dni)) {
                dtm.addRow(a.DatostoArray());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }
    void Imprimir() {
        try {
            //creamos un documento
            Document doc = new Document();
            OutputStream archivo = new FileOutputStream("Consulta.pdf");
            PdfWriter.getInstance(doc, archivo);

            //abrimos el archivo
            doc.open();
            Paragraph encabezado = new Paragraph("Consulta de pagos", FontFactory.getFont("arial", 14, Font.BOLD));
            encabezado.setAlignment(encabezado.ALIGN_CENTER);
            Paragraph separacion = new Paragraph("\n");
            Paragraph alumno = new Paragraph("Alumno: " + tbxdni.getText().toUpperCase());
//            Paragraph fecha = new Paragraph("fecha y Hora: " + tbxfecha.getText() + " " + tbxhora.getText());
            //creamos la tabla para las filas del jtable
            PdfPTable tabla = new PdfPTable(5);
            int[] ancho = new int[]{180, 80, 150,100,80};
            tabla.setSpacingBefore(50);
            tabla.setWidthPercentage(80);
            tabla.addCell("Nombres");
            tabla.addCell("DNI");
            tabla.addCell("Nivel");
            tabla.addCell("Concepto");
            tabla.addCell("Fecha de pago");

            int filas = jTable1.getRowCount();
            for (int i = 0; i < filas; i++) {
                tabla.addCell(dtm.getValueAt(i, 0).toString());
                tabla.addCell(dtm.getValueAt(i, 1).toString());
                tabla.addCell(dtm.getValueAt(i, 2).toString());
                tabla.addCell(dtm.getValueAt(i, 3).toString());
                tabla.addCell(dtm.getValueAt(i, 4).toString());
            }
            //le aÃ±adimos los elementos al documento
            doc.add(encabezado);
            doc.add(separacion);
            doc.add(alumno);
//            doc.add(fecha);
            doc.add(tabla);
            doc.close();
            archivo.close();

            //abrir el documento
            File arch = new File("Consulta.pdf");
            Desktop.getDesktop().open(arch);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al generar boleta");
        }


    }
