    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        try {
            if (FIListaMatriculas.modif) {
                for (Matricula m : ADNMatricula.ListaTotalDatos(idmatricula)) {
                    tbxalumno.setText(nomalu);
                    tbxperiodo.setText(m.getPeriodo());
                    tbxseccion.setText(m.getSeccion());
                    cbxniveles.addItem(nivel);
                    String fecha=Formatos.sdtfecha.format(m.getFecha());
                    tbxfecha.setText(fecha);
                    tbxhora.setText(m.getHora());
                }
            } else {
                String fecha1 = Formatos.sdtfecha.format(new java.util.Date());
                tbxfecha.setText(fecha1);
                tbxperiodo.setText("2013");
                CargarComboNiveles();
                Calendar calendario = new GregorianCalendar();
                int hora = calendario.get(Calendar.HOUR_OF_DAY);
                int minutos = calendario.get(Calendar.MINUTE);
                tbxhora.setText(hora + ":" + minutos);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }

    }//GEN-LAST:event_formInternalFrameOpened

