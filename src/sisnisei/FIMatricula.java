    public static int idalu = 0, idmatricula = 0;
    public static String nomalu = "", nivel = "";

    public void CargarComboNiveles() throws ClassNotFoundException, SQLException {
        cbxniveles.removeAllItems();
        for (Nivel c : ADNNivel.CargarCombo()) {
            cbxniveles.addItem(c.getIdNivel() + "-" + c.getGrado());
        }
    }

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            String nivel = cbxniveles.getSelectedItem().toString();
            StringTokenizer guion = new StringTokenizer(nivel, "-");
            int idnivel = Integer.parseInt(guion.nextToken().toString());
            String per = tbxperiodo.getText().trim();
            String sec = tbxseccion.getText().trim();
            String fec = tbxfecha.getText().trim();
            java.sql.Date fecsql = Formatos.Fechasql(fec);
            String hor = tbxhora.getText();
            boolean gua = ADNMatricula.Guardar(new Matricula(idmatricula, idalu, idnivel, per, sec, fecsql, hor));
            if (gua) {
                if (FIListaEmpleados.modif) {
                    JOptionPane.showMessageDialog(rootPane, "Datos actualizados correctamente");
                    FIListaEmpleados.modif = false;
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Datos guardados correctamente");
                }
                setVisible(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

