    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        try {
            String nivel = cbxnivel.getSelectedItem().toString();
            StringTokenizer guion = new StringTokenizer(nivel, "-");
            String curso = cbxcurso.getSelectedItem().toString();
            int idnivel = Integer.parseInt(guion.nextToken().toString());
            Curso a = new Curso();
            a.setNombre(curso);

            int idcurso = ADNCurso.ObtenerIdCurso(a);
            String dia = cbxdia.getSelectedItem().toString();
            String horaini = ftbxhoraini.getText();
            String horafin = ftbxhorafin.getText();

            String[] array = new String[6];
            array[0] = String.valueOf(idnivel);
            array[1] = String.valueOf(idcurso);
            array[2] = dia;
            array[3] = horaini;
            array[4] = horafin;
            array[5] = String.valueOf(iddocente);
            dtm = (DefaultTableModel) jTable1.getModel();
            int filas = jTable1.getRowCount();
            //Validar el ingreso de filas al jtable
            boolean estado = false;
            for (int i = 0; i < filas; i++) {
                if (dia == dtm.getValueAt(i, 2).toString() && idcurso == Integer.parseInt(dtm.getValueAt(i, 1).toString()) && idnivel == Integer.parseInt(dtm.getValueAt(i, 0).toString())) {
                    JOptionPane.showMessageDialog(rootPane, "Registro repetido");
                    estado = true;
                }
            }
            if (estado == false) {
                dtm.addRow(array);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }

    }//GEN-LAST:event_btnagregarActionPerformed

