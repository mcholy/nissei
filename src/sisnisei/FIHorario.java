    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        try {
            int filas = jTable1.getRowCount();
            boolean gua = false;
            for (int i = 0; i < filas; i++) {
                int idnivel = Integer.parseInt(dtm.getValueAt(i, 0).toString());
                int idcurso = Integer.parseInt(dtm.getValueAt(i, 1).toString());
                String dia = dtm.getValueAt(i, 2).toString();
                String horaini = dtm.getValueAt(i, 3).toString();
                String horafin = dtm.getValueAt(i, 4).toString();
                int iddoc = Integer.parseInt(dtm.getValueAt(i, 5).toString());

                gua = ADNHorario.Guardar(new Horario(0, idnivel, idcurso, dia, horaini, horafin, iddoc));
            }
            if (gua) {
                JOptionPane.showMessageDialog(rootPane, "Registros guardados correctamente");
                Limpiar();
                HabilitarComponentes(false);
                HabilitarBotones(true, false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }

    }//GEN-LAST:event_btnguardarActionPerformed

