    public void CargarComboNiveles() throws ClassNotFoundException, SQLException {
        cbxnivel.removeAllItems();
        String grado = cbxgrado.getSelectedItem().toString();
        for (Nivel c : ADNNivel.CargarComboporGrado(grado)) {
            cbxnivel.addItem(c.getIdNivel() + "-" + c.getGrado());
        }
    }

    public void CargarComboCursos() throws ClassNotFoundException, SQLException {
        cbxcurso.removeAllItems();
        for (Curso c : ADNCurso.CargarCombo()) {
            cbxcurso.addItem(c.getNombre());
        }
    }

    void Limpiar() {
        try {
//            int items=cbxgrado.getItemCount();
//            for (int i = 0; i <= items-1; i++) {
//                cbxgrado.removeItemAt(0);
//            }
            cbxcurso.removeAllItems();
            tbxdocente.setText("");
            iddocente = 0;
            cbxnivel.removeAllItems();
            ftbxhoraini.setText("");
            ftbxhorafin.setText("");
            int filas = jTable1.getRowCount();
            for (int i = 0; i < filas; i++) {
                dtm.removeRow(0);
            }
        } catch (Exception e) {
        }

    }

